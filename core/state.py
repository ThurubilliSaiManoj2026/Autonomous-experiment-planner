# core/state.py
# ─────────────────────────────────────────────────────────────────────────────
# Defines the shared state object that flows through the entire LangGraph
# pipeline. Every agent reads from this state and writes updates back to it.
#
# Think of this as the "shared briefing document" that all three agents
# collaborate on. The Literature Retrieval Agent writes the papers and
# retrieval context. The Analysis Agent reads that context and writes
# the gaps and hypothesis. The Planning Agent reads the hypothesis and
# writes the final experimental plan. LangGraph manages the passing of
# this state object between nodes automatically.
#
# We use TypedDict so that:
#   1. Every field has an explicit type — no ambiguity between agents
#   2. Python type checkers and IDEs can catch mistakes early
#   3. The state is self-documenting — any developer can read this file
#      and immediately understand what data the system tracks
# ─────────────────────────────────────────────────────────────────────────────

from typing import TypedDict, List, Optional, Dict, Any


class ResearchPaper(TypedDict):
    """
    Represents a single research paper fetched from ArXiv.
    Each field maps directly to metadata returned by the ArXiv API.
    """
    paper_id: str        # ArXiv paper ID, e.g. "2301.07041"
    title: str           # Full title of the paper
    authors: List[str]   # List of author names
    abstract: str        # Paper abstract text
    published: str       # Publication date as a string, e.g. "2023-01-17"
    url: str             # Direct link to the paper on ArXiv


class ResearchGap(TypedDict):
    """
    Represents a single identified gap in the existing literature.

    The Analysis Agent produces exactly 4 of these after reasoning over
    the retrieval context. Each gap has a short title (a concise phrase
    naming the gap), a description (what is missing and why it exists),
    an explanation of importance (why this gap matters to the field),
    supporting evidence from the retrieved literature, and a severity
    rating. The title and importance fields were added to support the
    deeper, more structured gap analysis required for academic evaluation.
    """
    gap_id: str               # Identifier, e.g. "GAP_001"
    title: str                # Short phrase naming the gap, e.g.
                              # "Cross-Domain Generalization Deficit"
    description: str          # What the gap is and why it exists
                              # (2-3 sentences)
    importance: str           # Why this gap matters to the field
                              # (1-2 sentences)
    supporting_evidence: str  # Evidence from the retrieved literature
                              # that confirms this gap exists
    severity: str             # "high", "medium", or "low"


class Hypothesis(TypedDict):
    """
    Represents the scientific hypothesis generated by the Analysis Agent.
    A hypothesis must be specific, testable, and directly address one gap.
    """
    statement: str        # The actual hypothesis statement
    rationale: str        # Why this hypothesis follows from the gap
    based_on_gap_id: str  # Which ResearchGap this hypothesis addresses


class DatasetSuggestion(TypedDict):
    """
    Represents a publicly available dataset suggested by the Planning Agent.
    """
    name: str          # Dataset name, e.g. "ISIC 2019"
    description: str   # What the dataset contains and why it fits
    url: str           # Link to access or download the dataset
    size: str          # Approximate size or number of samples


class ExperimentalPlan(TypedDict):
    """
    The final structured output produced by the Planning Agent.
    This is the deliverable — what the system ultimately generates.
    """
    objective: str                              # One-sentence experimental goal
    methodology: str                            # Step-by-step approach
    proposed_architecture: str                  # Model architecture recommendation
    evaluation_metrics: List[str]               # Metrics to measure success
    baseline_comparisons: List[str]             # Existing methods to compare against
    suggested_datasets: List[DatasetSuggestion] # Real datasets to use
    expected_contribution: str                  # New knowledge this adds
    estimated_timeline: str                     # Phased timeline estimate


class AgentState(TypedDict):
    """
    THE MASTER STATE OBJECT — this is what flows through the LangGraph.

    Every agent node receives this state as input and returns a dictionary
    containing only the fields it updated. LangGraph merges those updates
    back into the master state before passing it to the next node.

    Field progression through the pipeline:
        research_topic         → SET by user input before graph starts
        ↓
        fetched_papers         → SET by Literature Retrieval Agent
        indexed_paper_ids      → SET by Literature Retrieval Agent
        retrieval_context      → SET by Literature Retrieval Agent
        ↓
        identified_gaps        → SET by Analysis Agent (exactly 4 gaps)
        selected_gap           → SET by Analysis Agent (primary gap)
        hypothesis             → SET by Analysis Agent
        ↓
        experimental_plan      → SET by Planning Agent
        ↓
        error_message          → SET by any agent on failure
        current_stage          → UPDATED by each agent
        is_complete            → SET True by Planning Agent on success
    """

    # ── Input ─────────────────────────────────────────────────────────────
    research_topic: str

    # ── Literature Retrieval Agent outputs ────────────────────────────────
    fetched_papers: List[ResearchPaper]
    indexed_paper_ids: List[str]
    retrieval_context: Dict[str, str]

    # ── Analysis Agent outputs ─────────────────────────────────────────────
    identified_gaps: List[ResearchGap]
    selected_gap: Optional[ResearchGap]
    hypothesis: Optional[Hypothesis]

    # ── Planning Agent outputs ─────────────────────────────────────────────
    experimental_plan: Optional[ExperimentalPlan]

    # ── Pipeline Control ───────────────────────────────────────────────────
    current_stage: str
    error_message: Optional[str]
    is_complete: bool