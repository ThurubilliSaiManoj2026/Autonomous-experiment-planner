def run_pipeline(research_topic: str) -> Generator:
    """
    Executes the three-agent pipeline and yields incremental progress
    updates so the interface reflects each stage as it completes.

    Yields a 7-tuple on every update:
        (status, papers, gaps, hypothesis, plan, metrics, timeline)
    """

    if not research_topic or not research_topic.strip():
        yield (
            "Please enter a research topic before clicking Run.",
            "", "", "", "", "", ""
        )
        return

    topic = research_topic.strip()

    # Stage 1
    yield (
        f"Stage 1 of 3 — Literature Retrieval\n\n"
        f"Querying ArXiv for papers on: {topic}\n"
        f"Building vector embeddings and indexing into ChromaDB.\n"
        f"This takes approximately 20 to 40 seconds.",
        "Retrieving papers from ArXiv...", "", "", "", "", ""
    )

    try:
        state = get_initial_state(topic)
        retrieval_result = run_retrieval_agent(state)
        state = {**state, **retrieval_result}
    except Exception as e:
        yield (
            f"Stage 1 failed — Retrieval error\n\n{str(e)}",
            f"Error detail:\n{traceback.format_exc()}", "", "", "", "", ""
        )
        return

    if state.get("error_message"):
        yield (f"Stage 1 failed\n\n{state['error_message']}", "", "", "", "", "", "")
        return

    papers = state.get("fetched_papers", [])
    papers_text = format_papers_output(papers)

    # Stage 2
    yield (
        f"Stage 2 of 3 — Gap Analysis\n\n"
        f"Running {Config.LOCAL_MODEL_NAME} locally via Ollama.\n"
        f"Identifying 4 research gaps and generating a hypothesis.\n"
        f"This takes 1 to 3 minutes on CPU — please wait.",
        papers_text,
        "Analysis in progress...", "", "", "", ""
    )

    try:
        analysis_result = run_analysis_agent(state)
        state = {**state, **analysis_result}
    except Exception as e:
        yield (
            f"Stage 2 failed — Analysis error\n\n{str(e)}",
            papers_text, f"Error: {str(e)}", "", "", "", ""
        )
        return

    if state.get("error_message"):
        yield (f"Stage 2 failed\n\n{state['error_message']}", papers_text, "", "", "", "", "")
        return

    gaps_text = format_gaps_output(state)
    hypothesis_text = format_hypothesis_output(state)

    # Stage 3
    yield (
        f"Stage 3 of 3 — Experimental Planning\n\n"
        f"Calling Groq API with {Config.GROQ_MODEL_NAME}.\n"
        f"Generating the complete experimental blueprint.\n"
        f"This takes approximately 5 to 15 seconds.",
        papers_text, gaps_text, hypothesis_text,
        "Generating experimental plan...", "", ""
    )

    try:
        planning_result = run_planning_agent(state)
        state = {**state, **planning_result}
    except Exception as e:
        yield (
            f"Stage 3 failed — Planning error\n\n{str(e)}",
            papers_text, gaps_text, hypothesis_text, f"Error: {str(e)}", "", ""
        )
        return

    if state.get("error_message"):
        yield (
            f"Stage 3 failed\n\n{state['error_message']}",
            papers_text, gaps_text, hypothesis_text, "", "", ""
        )
        return

    # Complete
    plan = state.get("experimental_plan") or {}
    plan_text = format_plan_output(plan)
    metrics_text = format_metrics_output(plan)
    timeline_text = format_timeline_output(plan)
    paper_count = len(papers)
    gap_count = len(state.get("identified_gaps", []))

    yield (
        f"Pipeline complete.\n\n"
        f"Papers retrieved and indexed: {paper_count}\n"
        f"Research gaps identified: {gap_count}\n"
        f"Hypothesis generated by: {Config.LOCAL_MODEL_NAME} (local, offline)\n"
        f"Experimental plan generated by: {Config.GROQ_MODEL_NAME} via Groq\n\n"
        f"Scroll down to review the full experimental proposal.",
        papers_text, gaps_text, hypothesis_text,
        plan_text, metrics_text, timeline_text
    )


