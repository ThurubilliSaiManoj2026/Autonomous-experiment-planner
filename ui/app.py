def format_papers_output(papers: list) -> str:
    if not papers:
        return "No papers were retrieved."
    lines = [f"## Retrieved Literature — {len(papers)} Papers from ArXiv\n"]
    for i, paper in enumerate(papers, 1):
        title = paper.get("title", "Untitled")
        authors = paper.get("authors", [])
        published = paper.get("published", "")[:4]
        url = paper.get("url", "")
        abstract = paper.get("abstract", "")[:220]
        if len(authors) > 3:
            author_str = f"{authors[0]}, {authors[1]}, {authors[2]} et al."
        elif authors:
            author_str = ", ".join(authors)
        else:
            author_str = "Authors not listed"
        lines.append(
            f"**{i}.** [{title}]({url})  \n"
            f"{author_str}. {published}.  \n"
            f"*{abstract}...*\n"
        )
    return "\n".join(lines)


def format_gaps_output(state: dict) -> str:
    gaps = state.get("identified_gaps", [])
    selected_gap = state.get("selected_gap", {})
    if not gaps:
        return "No research gaps were identified."
    lines = [f"## Research Gap Analysis — {len(gaps)} Gaps Identified\n"]
    severity_label = {"high": "High", "medium": "Medium", "low": "Low"}
    for i, gap in enumerate(gaps, 1):
        gap_id = gap.get("gap_id", f"GAP_00{i}")
        title = gap.get("title", "Research Gap")
        description = gap.get("description", "")
        importance = gap.get("importance", "")
        evidence = gap.get("supporting_evidence", "")
        severity = gap.get("severity", "medium").lower()
        label = severity_label.get(severity, "Medium")
        is_primary = selected_gap and gap_id == selected_gap.get("gap_id")
        primary_note = (
            "  —  *Selected as primary gap for hypothesis generation*"
            if is_primary else ""
        )
        lines.append(
            f"### {gap_id}: {title}\n"
            f"**Severity:** {label}{primary_note}\n\n"
            f"**Description:** {description}\n\n"
            f"**Why This Matters:** {importance}\n\n"
            f"**Supporting Evidence:** {evidence}\n"
        )
    return "\n".join(lines)


def format_hypothesis_output(state: dict) -> str:
    hypothesis = state.get("hypothesis")
    selected_gap = state.get("selected_gap", {})
    if not hypothesis:
        return "No hypothesis was generated."
    statement = hypothesis.get("statement", "")
    rationale = hypothesis.get("rationale", "")
    based_on = hypothesis.get("based_on_gap_id", "")
    gap_title = selected_gap.get("title", "") if selected_gap else ""
    gap_desc = selected_gap.get("description", "") if selected_gap else ""
    return (
        f"## Scientific Hypothesis\n"
        f"**Hypothesis Statement**\n\n"
        f"{statement}\n\n"
        f"**Addresses Gap:** {based_on}"
        + (f" — {gap_title}" if gap_title else "")
        + f"\n\n*{gap_desc}*\n\n"
        f"**Rationale**\n\n"
        f"{rationale}\n\n"
        f"*Generated by {Config.LOCAL_MODEL_NAME} running locally via "
        f"Ollama (offline inference — no cloud API required for this stage).*"
    )


def format_plan_output(plan: dict) -> str:
    if not plan:
        return "No experimental plan was generated."
    objective = plan.get("objective", "")
    methodology = plan.get("methodology", "")
    architecture = plan.get("proposed_architecture", "")
    contribution = plan.get("expected_contribution", "")
    datasets = plan.get("suggested_datasets", [])
    dataset_lines = []
    for d in datasets:
        name = d.get("name", "")
        desc = d.get("description", "")
        url = d.get("url", "")
        size = d.get("size", "")
        dataset_lines.append(f"**[{name}]({url})** ({size})  \n{desc}")
    datasets_text = "\n\n".join(dataset_lines) if dataset_lines else "No datasets specified."
    return (
        f"## Experimental Plan\n"
        f"### Objective\n{objective}\n\n"
        f"### Methodology\n{methodology}\n\n"
        f"### Proposed Architecture\n{architecture}\n\n"
        f"### Recommended Datasets\n{datasets_text}\n\n"
        f"### Expected Scientific Contribution\n{contribution}\n\n"
        f"*Generated by {Config.GROQ_MODEL_NAME} via Groq API (cloud inference).*"
    )


def format_metrics_output(plan: dict) -> str:
    if not plan:
        return ""
    metrics = plan.get("evaluation_metrics", [])
    baselines = plan.get("baseline_comparisons", [])
    lines = ["## Evaluation Metrics\n"]
    for m in metrics:
        lines.append(f"- {m}")
    lines.append("\n## Baseline Comparisons\n")
    for b in baselines:
        lines.append(f"- {b}")
    return "\n".join(lines)


def format_timeline_output(plan: dict) -> str:
    if not plan:
        return ""
    timeline = plan.get("estimated_timeline", "")
    if not timeline:
        return "No timeline was provided."
    phases = timeline.split("Phase ")
    formatted = [f"**Phase {p.strip()}**" for p in phases if p.strip()]
    return "## Estimated Timeline\n\n" + "\n\n".join(formatted)


